{
    use Queueable, SerializesModels;
    public $data;

    /**
     * Create a new message instance.
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->view('emails.orders.today-recovery-alert')
                    ->subject("[Alerte de reprise] Retours de location prÃ©vus aujourd'hui")
                    ->with(['data' => $this->data]);
    }
}
